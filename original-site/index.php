		<span style="font-size: 20px; color: #000;"><strong>About DASNA:</strong></span></center>
		<br/>
				<p style="text-align: justify">The Development Association of Shire (DAS) was first established in 2004 by the dedicated people of United States of America. DAS was formed as an associational structure and operational working manuals, which made it transparent and accountable to the supporters and the general public. It has been licensed as non-profit organization by the State Of Massachusetts in the city of Boston. The main objective was to expand and strengthen some of the needs that were not addressed by the government and/or <abbr title="Non-Governmental Organization">NGO</abbr>'s such as:<br/></p><p style="text-align:left">
		- to preserve the historical sites and cultural landscapes, and to ensure the highest level of protections<br/>
		- to improve the quality of education and health care in the region<br/>
		- to enhance/upsurge city structures<br/>
		- to support the youth and sports affairs<br/>
		- to participate actively in the community<br/></p>
		<p style="text-align: justify">After the establishment of DAS the organization expanded its branches globally. The Development Association of Shire in North America was established in 2004. The objectives remained the same to outreach to under privileged people. DASNA has carried out six fundraising events that accumulated over 78,000 USD to fund the current library project. The cost of the new library facility cost approximately 3.7 million Eth. Birr (approximately 370,000USD). The library has a two story building and also includes a recreational area for the youth, a research center, and is handicap accessible.</p><br/>
